class Solution
{
public:
    int rob(vector<int> &nums)
    {
        int n = nums.size();
        vector<int> dp(n + 2, 0);
        dp[n] = 0;
        int dp_i2 = 0;
        int dp_i1 = 0;
        int dp_i = 0;
        for (int i = n - 1; i >= 0; i--)
        {
            //int done = dp[i + 2] + nums[i];
            int done = dp_i2 + nums[i];
            //int notdone = dp[i + 1];
            int notdone = dp_i1;
            dp_i = max(done, notdone);
            dp_i2 = dp_i1;
            dp_i1 = dp_i;
        }

        return dp_i;
    }
};
